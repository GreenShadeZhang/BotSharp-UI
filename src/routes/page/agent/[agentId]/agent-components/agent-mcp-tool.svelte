<script>
    import { onMount } from 'svelte';
	import { getServerConfigs } from '$lib/services/mcp-service';
    import MaterialCard from '$lib/common/MaterialCard.svelte';
    import MaterialButton from '$lib/common/MaterialButton.svelte';
    import MaterialTextField from '$lib/common/MaterialTextField.svelte';
    
    const limit = 10;
    const textLimit = 100;

    /** @type {import('$agentTypes').AgentModel} */
    export let agent;

    /** @type {() => void} */
    export let handleAgentChange = () => {};

    export const fetchMcpTools = () => {
        const candidates = innerMcps?.filter(x => !!x.name)?.map(x => {
            /** @type {import('$commonTypes').NameBase[]} */
            const functions = [];

            const uniqueFns = new Set();
            const fns = x.functions.filter(f => !!f.name);

            fns.forEach(f => {
                if (!uniqueFns.has(f.name)) {
                    functions.push({ ...f });
                    uniqueFns.add(f.name);
                }
            });

           return {
                ...x,
                name: x.name,
                disabled: x.disabled,
                functions: functions,
           };
        });

        /** @type {import('$agentTypes').AgentMcpTool[]} */
        const tools = [];
        const unique = new Set();
        candidates.forEach(x => {
            if (!unique.has(x.name)) {
                tools.push(x);
                unique.add(x.name);
            }
        });

        innerRefresh(tools);
        return tools;
    }

    export const fetchOriginalMcpTools = () => {
        return innerMcps;
    }

    export const refresh = () => init();

    /** @type {any[]} */
    let mcpOptions = [];

    /** @type {import('$agentTypes').AgentMcpTool[]} */
    let innerMcps = [];


    onMount(async () => {
        getServerConfigs().then(res => {
            const list = res?.map(x => {
                return {
                    id: x.id,
                    name: x.name
                };
            }) || [];
            mcpOptions = [
                {
                    id: '',
                    name: ''
                },
                ...list
            ];
        }).catch(() => {
            mcpOptions = [];
        });
        init();
    });

    function init() {
        const list = agent.mcp_tools?.map(x => {
            return {
                ...x,
                displayName: ""
            };
        }) || [];
        innerRefresh(list);
    }

    /** @param {import('$agentTypes').AgentMcpTool[]} list */
    function innerRefresh(list) {
        innerMcps = list?.map(x => {
            return {
                name: x.name,
                server_id: x.server_id,
                disabled: x.disabled,
                functions: x.functions?.map(x => ({ ...x })) || []
            }
        }) || [];
    }

    /**
	 * @param {any} e
	 * @param {number} idx
	 */
     function changeTool(e, idx) {
        const found = innerMcps.find((_, index) => index === idx);
        if (!found) return;
        
        const val = e.target.value;
        const name = mcpOptions.find(x => x.id == val)?.name || '';

        found.name = name;
        found.server_id = val;
        innerRefresh(innerMcps);
        handleAgentChange();
    }

    function addTool() {
        innerMcps = [
            ...innerMcps,
            {
                name: '',
                server_id: '',
                disabled: false,
                functions: []
            }
        ];
        handleAgentChange();
    }

    /** @param {number} idx */
    function deleteTool(idx) {
        innerMcps = innerMcps.filter((_, index) => index !== idx);
        handleAgentChange();
    }

    /**
     * @param {any} e
	 * @param {number} uid
	 */
    function toggleTool(e, uid) {
        const found = innerMcps.find((_, index) => index === uid);
        if (!found) return;

        found.disabled = !e.target.checked;
        innerRefresh(innerMcps);
        handleAgentChange();
    }

    /**
     * @param {any} e
	 * @param {number} tid
     * @param {number} id
     * @param {string} type
	 */
    function changeContent(e, tid, id, type) {
        const found = innerMcps.find((_, index) => index === tid);
        if (!found) return;

        const val = e.target.value;
        if (type === 'function') {
            const fn = found.functions.find((_, index) => index == id);
            if (fn) {
                fn.name = val;
            }
        }

        innerRefresh(innerMcps);
        handleAgentChange();
    }

    /**
	 * @param {number} id
	 * @param {string} type
	 */
     function addToolContent(id, type) {
        const found = innerMcps.find((_, index) => index === id);
        if (!found || found.disabled) return;

        if (type === 'function') {
            found.functions.push({ name: '' });
        }

        innerRefresh(innerMcps);
        handleAgentChange();
    }

    /**
	 * @param {number} uid
	 * @param {number} id
     * @param {string} type
	 */
     function deleteToolContent(uid, id, type) {
        const found = innerMcps.find((_, index) => index === uid);
        if (!found || found.disabled) return;

        if (type === 'function') {
            const fns = found.functions?.filter((_, index) => index !== id) || [];
            found.functions = fns;
        }
        
        innerRefresh(innerMcps);
        handleAgentChange();
    }
</script>

<MaterialCard variant="outlined" className="agent-mcp-tool-card">
    <div class="material-card-content">
        <div class="text-center mb-4">
            <h5 class="material-heading">MCP Tools</h5>
            <h6 class="material-body-medium text-outline">Tools powered by MCP Servers</h6>
        </div>

        <div class="material-mcp-container">
            {#each innerMcps as tool, uid (uid)}
                <div class="utility-wrapper">
                    <div class="utility-row utility-row-primary">
                        <div class="utility-label fw-bold">
                            <div class="line-align-center">{`MCP #${uid + 1}`}</div>
                            <div class="utility-tooltip">
                                <div class="line-align-center">
                                    <input
                                        class="material-checkbox"
                                        type="checkbox"
                                        checked={!tool.disabled}
                                        on:change={e => toggleTool(e, uid)}
                                    />
                                </div>
                                <div
                                    class="line-align-center"
                                    data-bs-toggle="tooltip"
                                    data-bs-placement="top"
                                    title="Uncheck to disable MCP"
                                >
                                    <i class="mdi mdi-information-outline text-outline" />
                                </div>
                            </div>
                        </div>
                        <div class="utility-value">
                            <div class="utility-input line-align-center">
                                <select
                                    class="material-select"
                                    disabled={tool.disabled}
                                    on:change={e => changeTool(e, uid)}
                                >
                                    {#each [...mcpOptions] as option}
                                        <option value={option.id} selected={option.id == tool.server_id}>
                                            {option.displayName || option.name}
                                        </option>
                                    {/each}
                                </select>
                            </div>
                            <div class="utility-delete line-align-center">
                                <i
                                    class="mdi mdi-close-circle text-danger clickable"
                                    role="link"
                                    tabindex="0"
                                    on:keydown={() => {}}
                                    on:click={() => deleteTool(uid)}
                                />
                            </div>
                        </div>
                    </div>

                    <div class="utility-row utility-row-secondary">
                        <div class="utility-content">
                            <div class="utility-list-item">
                                <div class="utility-label line-align-center">
                                    {'Server'}
                                </div>
                                <div class="utility-value">
                                    <div class="utility-input line-align-center">
                                        <MaterialTextField
                                            type="text"
                                            disabled={true}
                                            value={tool.server_id}
                                            placeholder="Server ID"
                                        />
                                    </div>
                                    <div class="utility-delete line-align-center"></div>
                                </div>
                            </div>
                        </div>
                        <div class="utility-content">
                            {#each tool.functions as fn, fid (fid)}
                                <div class="utility-list-item">
                                    <div class="utility-label line-align-center">
                                        {fid === 0 ? 'Tools' : ''}
                                    </div>
                                    <div class="utility-value">
                                        <div class="utility-input line-align-center">
                                            <MaterialTextField
                                                type="text"
                                                disabled={tool.disabled}
                                                value={fn.name}
                                                placeholder="Function name..."
                                                on:change={e => changeContent(e, uid, fid, 'function')}
                                            />
                                        </div>
                                        <div class="utility-delete line-align-center">
                                            <i
                                                class="mdi mdi-close-circle text-danger clickable"
                                                role="link"
                                                tabindex="0"
                                                on:keydown={() => {}}
                                                on:click={() => deleteToolContent(uid, fid, 'function')}
                                            />
                                        </div>
                                    </div>
                                </div>
                            {/each}

                            {#if tool.functions?.length < limit}
                                <div class="utility-list-item">
                                    <div class="utility-label">
                                        {tool.functions.length === 0 ? 'Functions' : ''}
                                    </div>
                                    <div class="utility-value">
                                        <i
                                            class="mdi mdi-plus-circle add-list clickable"
                                            role="link"
                                            tabindex="0"
                                            on:keydown={() => {}}
                                            on:click={() => addToolContent(uid, 'function')}
                                        />
                                    </div>
                                </div>
                            {/if}
                        </div>
                    </div>
                </div>
            {/each}

            {#if innerMcps.length < limit}
                <div class="material-add-mcp mt-3">
                    <MaterialButton 
                        variant="filled" 
                        icon="mdi mdi-plus"
                        on:click={() => addTool()}
                    >
                        Add MCP
                    </MaterialButton>
                </div>
            {/if}
        </div>
    </div>
</MaterialCard>